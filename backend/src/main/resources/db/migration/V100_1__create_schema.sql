    PRIMARY KEY (id)
);

CREATE TABLE event_participants (
    id              INTEGER GENERATED BY DEFAULT AS IDENTITY,
    event_id        INTEGER,
    participant_id  INTEGER,
    created_at      TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at      TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    created_by      INTEGER,
