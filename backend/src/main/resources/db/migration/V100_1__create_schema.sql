    PRIMARY KEY (id)
);

CREATE TABLE quotes (
    id              INTEGER GENERATED BY DEFAULT AS IDENTITY,
    title           VARCHAR(255) NOT NULL,
    description     TEXT,
    status          VARCHAR(50),
    subtotal        DECIMAL(10, 2),
    tax             INTEGER,
    total           DECIMAL(10, 2),
    contact_id      INTEGER,
    company_id      INTEGER,
    created_at      TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at      TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    created_by      INTEGER,
    updated_by      INTEGER,
    PRIMARY KEY (id)
);
COMMENT ON COLUMN quotes.status IS 'DRAFT, SENT, ACCEPTED';

CREATE TABLE quote_items (
    id              INTEGER GENERATED BY DEFAULT AS IDENTITY,
    quote_id        INTEGER,
    title           VARCHAR(255) NOT NULL,
    unit_price      DECIMAL(10,2),
    quantity        INTEGER,
    discount        INTEGER,
    total_price     DECIMAL(10,2),
    created_at      TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at      TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    created_by      INTEGER,
    updated_by      INTEGER,
    PRIMARY KEY (id)
);

CREATE TABLE audits (
    id             INTEGER GENERATED BY DEFAULT AS IDENTITY,
    target_id      INTEGER,
    target_entity  TEXT,
    action         TEXT NOT NULL,
    user_id        INTEGER,
    changes        JSONB,
    created_at     TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at     TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    created_by     INTEGER,
    updated_by     INTEGER,
    PRIMARY KEY(id)
);
